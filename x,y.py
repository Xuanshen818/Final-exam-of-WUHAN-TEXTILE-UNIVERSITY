'''
3.Python-选择结构-03(M)
【问题描述】输入两个整数x和y
如果 x ∈[10,20]，则将 x减5，y加1
否则，将y减1
求x与y的和并输出
【样例输入】
10
11
【样例输出】
17
'''

# 输入两个整数 x 和 y
x = int(input("请输入第一个整数 x: "))
y = int(input("请输入第二个整数 y: "))

# 检查 x 是否在区间 [10, 20] 内
if 10 <= x <= 20:
    x -= 5
    y += 1
else:
    y -= 1

# 计算 x 和 y 的和
result = x + y

# 输出结果
print(f"x 和 y 的和是: {result}")
